@implementation TemplateListController
@synthesize templates;
@synthesize nextView;


#pragma mark -
#pragma mark View lifecycle


- (void)viewDidLoad {
    [super viewDidLoad];

	NSArray *paths = [[NSBundle mainBundle] pathsForResourcesOfType:@"png" inDirectory:@"Templates"];
	self.templates = [NSMutableArray arrayWithCapacity:[paths count]];
	for (NSString *path in paths) {
		NSString *label = [path lastPathComponent];
		label = [label substringToIndex:[label length] - 4];
		[self.templates addObject:label];
	}
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
}


